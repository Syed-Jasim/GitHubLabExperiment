public class example
{
    static byte a;
    static short b;
    static int i;
    static char c;
    static float d;
    static double e;
    static boolean bl;
    public static void main(String[]args)
{
    System.out.println("Default value of byte type="+a);
    System.out.println("Default value of short type="+b);
    System.out.println("Default value of int type="+i);
    System.out.println("Default value of char type="+(int)c);
    System.out.println("Default value of float type="+d);
    System.out.println("Default value of double type="+e);
    System.out.println("Default value of boolean type="+bl);
  }	
}